class Alternative {
  constructor(
    id_alternatif,
    alternatif,
    vegetasi_area,
    volume_material,
    luas,
    volume_tampungan,
    lama_operasi,
    harga_air,
    akses_jalan
  ) {
    this.id_alternatif = id_alternatif;
    this.alternatif = alternatif;
    this.vegetasi_area = vegetasi_area;
    this.volume_material = volume_material;
    this.luas = luas;
    this.volume_tampungan = volume_tampungan;
    this.lama_operasi = lama_operasi;
    this.harga_air = harga_air;
    this.akses_jalan = akses_jalan;
  }
}

export default Alternative;
